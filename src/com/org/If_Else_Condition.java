package com.org;

public class If_Else_Condition {
	public static void main(String[] args) {
		int salary = 16;
		
	if (salary >= 1 & salary <= 5) {
			System.out.println("average pay");
			
		
		}
		else if (salary >= 6 & salary <= 11 ) {
			System.out.println("med pay");
			
		}
		else if (salary >= 12 & salary <=15) {
			System.out.println("normal pay");
		}
		else {
			System.out.println("perfect pay");
		}
	}

}
